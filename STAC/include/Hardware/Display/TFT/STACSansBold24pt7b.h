// Character positions (all contiguous, no remapping needed for these chars):
//   0x20 (pos  0):
//   0x21 (pos  1): *
//   0x22 (pos  2): +
//   0x23 (pos  3): -
//   0x24 (pos  4): 0
//   0x25 (pos  5): 1
//   0x26 (pos  6): 2
//   0x27 (pos  7): 3
//   0x28 (pos  8): 4
//   0x29 (pos  9): 5
//   0x2A (pos 10): 6
//   0x2B (pos 11): 7
//   0x2C (pos 12): 8
//   0x2D (pos 13): 9
//   0x2E (pos 14): ?
//   0x2F (pos 15): A
//   0x30 (pos 16): C
//   0x31 (pos 17): N
//   0x32 (pos 18): P
//   0x33 (pos 19): S
//   0x34 (pos 20): T

/**
 * @brief Remap standard ASCII/Unicode characters to STACSansBold24pt7b font positions
 *
 * This font is contiguous from 0x20-0x34 but contains only selected characters.
 * Characters are stored in a specific order, not by ASCII value.
 * This function maps standard ASCII input to the correct font position.
 *
 * @param unicode The standard ASCII/Unicode character code to remap
 * @return The remapped character position for the font, or 0x20 (space) if not found
 *
 * @example
 * char remapped = remapCharToSTACSansFont('N');  // Returns 0x30
 * canvas->print(remapped);  // Prints 'N' using the custom font
 */
inline char remapCharToSTACSansFont( uint16_t unicode ) {
    switch ( unicode ) {
        case 0x20:
            return 0x20; // space -> 0x20 (position 0)
        case '*':
            return 0x21; // * -> 0x21 (position 1)
        case '+':
            return 0x22; // + -> 0x22 (position 2)
        case '-':
            return 0x23; // - -> 0x23 (position 3)
        case '0':
            return 0x24; // 0 -> 0x24 (position 4)
        case '1':
            return 0x25; // 1 -> 0x25 (position 5)
        case '2':
            return 0x26; // 2 -> 0x26 (position 6)
        case '3':
            return 0x27; // 3 -> 0x27 (position 7)
        case '4':
            return 0x28; // 4 -> 0x28 (position 8)
        case '5':
            return 0x29; // 5 -> 0x29 (position 9)
        case '6':
            return 0x2A; // 6 -> 0x2A (position 10)
        case '7':
            return 0x2B; // 7 -> 0x2B (position 11)
        case '8':
            return 0x2C; // 8 -> 0x2C (position 12)
        case '9':
            return 0x2D; // 9 -> 0x2D (position 13)
        case '?':
            return 0x2E; // ? -> 0x2E (position 14)
        case 'A':
            return 0x2F; // A -> 0x2F (position 15)
        case 'C':
            return 0x30; // C -> 0x30 (position 16)
        case 'N':
            return 0x31; // N -> 0x31 (position 17)
        case 'P':
            return 0x32; // P -> 0x32 (position 18)
        case 'S':
            return 0x33; // S -> 0x33 (position 19)
        case 'T':
            return 0x34; // T -> 0x34 (position 20)
        default:
            return 0x20; // fallback to space
    }
}
